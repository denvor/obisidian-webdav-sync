// main.ts
var WebdavSyncPlugin = class extends import_obsidian3.Plugin {
  constructor() {
    super(...arguments);
    this.client = null;
    this.tracker = null;
    this.logger = null;
    this.engine = null;
    this.intervalId = null;
    this.saveSyncHandler = null;
    this.statusBarItem = null;
    this.ribbonIcon = null;
  }
  // ==================== 生命周期 ====================
  async onload() {
    await this.loadSettings();
    this.logger = new Logger(this.app.vault);
    this.logger.info("\u63D2\u4EF6\u5DF2\u52A0\u8F7D");
    this.tracker = new FileTracker(this.app.vault);
    await this.tracker.load();
    this.initClient();
    this.initEngine();
    this.addSettingTab(new WebdavSyncSettingTab(this.app, this));
    this.addCommand({
      id: "sync-now",
      name: "Sync with WebDAV",
      callback: () => this.runSync()
    });
    this.statusBarItem = this.addStatusBarItem();
    this.statusBarItem.setText("WebDAV: \u5C31\u7EEA");
    this.ribbonIcon = this.addRibbonIcon(
      "cloud",
      "WebDAV \u8BBE\u7F6E",
      () => {
        const setting = this.app.setting;
        if (setting) {
          setting.open();
          setting.openTabById("obsidian-webdav-sync");
        }
      }
    );
    this.moveRibbonToBottom();
    this.updateSaveSyncListener();
    this.updateIntervalSync();
    if (this.settings.syncOnStartup && this.client) {
      setTimeout(() => {
        this.runSync();
      }, 5e3);
    }
  }
  onunload() {
    if (this.intervalId !== null) {
      window.clearInterval(this.intervalId);
      this.intervalId = null;
    }
    if (this.saveSyncHandler) {
      this.app.vault.off("modify", this.saveSyncHandler);
      this.saveSyncHandler = null;
    }
    this.logger?.flush();
  }
  // ==================== 设置管理 ====================
  async loadSettings() {
    this.settings = Object.assign({}, DEFAULT_SETTINGS, await this.loadData());
  }
  async saveSettings() {
    await this.saveData(this.settings);
    this.initClient();
    this.initEngine();
  }
  // ==================== 客户端初始化 ====================
  initClient() {
    if (this.settings.webdavUrl && this.settings.username) {
      this.client = new WebdavClient(
        this.settings.webdavUrl,
        this.settings.username,
        this.settings.password
      );
    } else {
      this.client = null;
    }
  }
  initEngine() {
    if (!this.client || !this.tracker || !this.logger)
      return;
    const resolver = new ConflictResolver(this.app.vault, this.client);
    this.engine = new SyncEngine(
      this.app.vault,
      this.client,
      this.tracker,
      resolver,
      this.settings,
      this.logger
    );
    this.engine.setOnProgress((current, total, message) => {
      if (total > 0) {
        this.statusBarItem?.setText(`WebDAV: ${message} (${current}/${total})`);
      } else {
        this.statusBarItem?.setText(`WebDAV: ${message}`);
      }
    });
  }
  /**
   * 将 Ribbon 图标移到最底部（设置齿轮上方）
   */
  moveRibbonToBottom() {
    if (!this.ribbonIcon)
      return;
    const retry = (attempt) => {
      if (attempt > 15)
        return;
      const ribbon = document.querySelector(".workspace-ribbon");
      if (!ribbon || !this.ribbonIcon) {
        setTimeout(() => retry(attempt + 1), 300);
        return;
      }
      const icons = ribbon.querySelectorAll(".clickable-icon");
      const sidebar = ribbon.querySelector(".sidebar-actions");
      const pluginIcons = Array.from(icons).filter(
        (icon) => !sidebar?.contains(icon)
      );
      const lastPluginIcon = pluginIcons[pluginIcons.length - 1];
      if (lastPluginIcon && this.ribbonIcon !== lastPluginIcon) {
        lastPluginIcon.parentNode?.insertBefore(
          this.ribbonIcon,
          lastPluginIcon.nextSibling
        );
      }
    };
    retry(0);
  }
  // ==================== 同步触发 ====================
  /**
   * 测试 WebDAV 连接
   */
  async testConnection() {
    if (!this.client) {
      return { ok: false, message: "\u8BF7\u5148\u914D\u7F6E\u670D\u52A1\u5668\u5730\u5740\u548C\u7528\u6237\u540D" };
    }
    return await this.client.testConnection();
  }
  /**
   * 执行完整同步
   */
  async runSync() {
    if (!this.engine) {
      new import_obsidian3.Notice("\u8BF7\u5148\u5728\u8BBE\u7F6E\u4E2D\u914D\u7F6E WebDAV \u8FDE\u63A5");
      return { success: false, message: "\u8BF7\u5148\u914D\u7F6E\u8FDE\u63A5" };
    }
    if (this.engine.isSyncing()) {
      new import_obsidian3.Notice("\u540C\u6B65\u5DF2\u5728\u8FDB\u884C\u4E2D");
      return { success: false, message: "\u540C\u6B65\u5DF2\u5728\u8FDB\u884C\u4E2D" };
    }
    this.ribbonIcon?.addClass("syncing");
    this.statusBarItem?.setText("WebDAV: \u540C\u6B65\u4E2D...");
    const result = await this.engine.sync();
    this.ribbonIcon?.removeClass("syncing");
    if (result.success) {
      this.statusBarItem?.setText("WebDAV: \u5DF2\u540C\u6B65");
      if (result.actions.length > 0) {
        new import_obsidian3.Notice(result.message);
      }
    } else {
      this.statusBarItem?.setText("WebDAV: \u9519\u8BEF");
      new import_obsidian3.Notice("\u540C\u6B65\u5931\u8D25: " + result.message);
    }
    setTimeout(() => {
      this.statusBarItem?.setText("WebDAV: \u5C31\u7EEA");
    }, 5e3);
    return result;
  }
  /**
   * 更新保存时同步监听器
   */
  updateSaveSyncListener() {
    if (this.saveSyncHandler) {
      this.app.vault.off("modify", this.saveSyncHandler);
      this.saveSyncHandler = null;
    }
    if (this.settings.syncOnSave && this.engine) {
      let timeoutId = null;
      this.saveSyncHandler = async () => {
        if (timeoutId !== null) {
          window.clearTimeout(timeoutId);
        }
        timeoutId = window.setTimeout(async () => {
          await this.runSync();
        }, 1e3);
      };
      this.app.vault.on("modify", this.saveSyncHandler);
    }
  }
  /**
   * 更新定时同步
   */
  updateIntervalSync() {
    if (this.intervalId !== null) {
      window.clearInterval(this.intervalId);
      this.intervalId = null;
    }
    if (this.settings.syncInterval > 0 && this.engine) {
      this.intervalId = window.setInterval(
        () => this.runSync(),
        this.settings.syncInterval * 60 * 1e3
      );
    }
  }
};
