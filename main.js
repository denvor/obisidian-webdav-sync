var LOG_DIR = ".obsidian/plugins/obsidian-webdav-sync";
var LOG_FILE = "sync.log";
var MAX_LINES = 500;
var Logger = class {
  constructor(vault) {
    this.logs = [];
    this.vault = vault;
  }
  /**
   * 写入一条日志
   */
  log(level, message) {
    const timestamp = new Date().toLocaleString("zh-CN", { hour12: false });
    const line = `[${timestamp}] [${level}] ${message}`;
    this.logs.push(line);
    if (level === "ERROR") {
      console.error(line);
    } else if (level === "WARN") {
      console.warn(line);
    } else {
      console.log(line);
    }
  }
  info(msg) {
    this.log("INFO", msg);
  }
  warn(msg) {
    this.log("WARN", msg);
  }
  error(msg) {
    this.log("ERROR", msg);
  }
  /**
   * 将所有日志写入文件（追加模式）
   */
  async flush() {
    if (this.logs.length === 0)
      return;
    try {
      await this.vault.adapter.mkdir(LOG_DIR);
      let existing = "";
      try {
        existing = await this.vault.adapter.read(`${LOG_DIR}/${LOG_FILE}`);
      } catch {
      }
      const allLines = existing ? existing.split("\n") : [];
      const newLines = this.logs;
      const merged = [...allLines, ...newLines];
      if (merged.length > MAX_LINES) {
        merged.splice(0, merged.length - MAX_LINES);
      }
      await this.vault.adapter.write(`${LOG_DIR}/${LOG_FILE}`, merged.join("\n") + "\n");
      this.logs = [];
    } catch (err) {
      console.error("\u5199\u5165\u65E5\u5FD7\u6587\u4EF6\u5931\u8D25:", err);
    }
  }
  /**
   * 获取日志文件路径（供设置界面显示"查看日志"用）
   */
  getLogFilePath() {
    return `${LOG_DIR}/${LOG_FILE}`;
  }
};

